package entity;

/**
 *
 * @author MSII
 */
public class Statistics {
    private int month;
    private int year;
    private int amount;

    public Statistics() {
    }

    public Statistics(int month, int year, int amount) {
        this.month = month;
        this.year = year;
        this.amount = amount;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
    
    
}
